let hari ="Kamis";

switch (hari) {
    case "Senin" :
        console.log("hari ini adalah hari pertama bekerja");
        break;
        case "Selasa" :
        console.log("hari ini adalah hari kedua bekerja");
        break;
        case "Rabu" :
        console.log("hari ini adalah hari ketiga bekerja");
        break;
        case "Kamis" :
        console.log("hari ini adalah hari keempat bekerja");
        break;
        case "Jumat" :
        console.log("hari ini adalah hari kelima bekerja");
        break;
        default :
        console.log("hari libur atau weekend")
}

